class NesColor {
  const NesColor(int rgb)
      : this.rgb = rgb,
        this.red = rgb >> 4 & 0xff,
        this.green = rgb >> 2 & 0xff,
        this.blue = rgb & 0xff;

  final int rgb;
  final int red;
  final int green;
  final int blue;
}

